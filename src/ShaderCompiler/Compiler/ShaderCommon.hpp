enum HCInputOutputType : uint16 {
	IO_INVALID = 0,
	IO_BOOL = (1 << 2),
	IO_SIGNED_BYTE = (1 << 3),
	IO_UNSIGNED_BYTE = (1 << 4),
	IO_SIGNED_SHORT = (1 << 5),
	IO_UNSIGNED_SHORT = (1 << 6),
	IO_SIGNED_INT = (1 << 7),
	IO_UNSIGNED_INT = (1 << 8),
	IO_HALF_FLOAT = (1 << 9),
	IO_FLOAT = (1 << 10),
	IO_DOUBLE_FLOAT = (1 << 11),
	IO_MAX = 0xFFC
};

enum HCInterpolationType : uint16 {
	INTERP_SMOOTH = 0,
	INTERP_FLAT = (1 << 12),
	INTERP_NO_PERSPECTIVE = (1 << 13),
	INTERP_CENTROID = (1 << 14),
	INTERP_SAMPLE = (1 << 15),
	INTERP_MAX = 0xF000
};

struct HCShaderVar {
	uint16 m_u16Type = 0;
	uint16 m_u16Flags = 0;
	uint32 m_arrData[8];
};

struct HCShaderVarLabelEntry {
	String m_strVarName;
	uint32 m_u32Index;
};

struct HCShaderVarTable {
	Array<HCShaderVarLabelEntry> m_vLabels;
	Array<HCShaderVar> m_vVars;
};

enum HCShaderFormat : uint8 {
	SHADER_FORMAT_GLSL,
	SHADER_FORMAT_HLSL,
	SHADER_FORMAT_SLANG,
	SHADER_FORMAT_INVALID
};

enum HCShaderStageType : uint8 {
	SHADER_STAGE_VERTEX,
	SHADER_STAGE_FRAGMENT,
	SHADER_STAGE_GEOMETRY,
	SHADER_STAGE_TESSELLATION_CONTROL,
	SHADER_STAGE_TESSELLATION_EVALUATION,
	SHADER_STAGE_COMPUTE,
	SHADER_STAGE_TASK,
	SHADER_STAGE_MESH,
	SHADER_STAGE_RAYGEN,
	SHADER_STAGE_ANY_HIT,
	SHADER_STAGE_CLOSEST_HIT,
	SHADER_STAGE_MISS,
	SHADER_STAGE_INTERSECTION,
	SHADER_STAGE_CALLABLE,
	SHADER_STAGE_INVALID
};

enum HCShaderStageBit : uint16 {
	SHADER_STAGE_INVALID_BIT = 0,
	SHADER_STAGE_VERTEX_BIT = (1 << 0),
	SHADER_STAGE_TESSELLATION_CONTROL_BIT = (1 << 1),
	SHADER_STAGE_TESSELLATION_EVALUATION_BIT = (1 << 2),
	SHADER_STAGE_GEOMETRY_BIT = (1 << 3),
	SHADER_STAGE_FRAGMENT_BIT = (1 << 4),
	SHADER_STAGE_COMPUTE_BIT = (1 << 5),
	SHADER_STAGE_TASK_BIT = (1 << 6),
	SHADER_STAGE_MESH_BIT = (1 << 7),
	SHADER_STAGE_RAYGEN_BIT = (1 << 8),
	SHADER_STAGE_ANY_HIT_BIT = (1 << 9),
	SHADER_STAGE_CLOSEST_HIT_BIT = (1 << 10),
	SHADER_STAGE_MISS_BIT = (1 << 11),
	SHADER_STAGE_INTERSECTION_BIT = (1 << 12),
	SHADER_STAGE_CALLABLE_BIT = (1 << 13),
	SHADER_STAGE_GRAPHICS_MIN = SHADER_STAGE_VERTEX_BIT,
	SHADER_STAGE_GRAPHICS_MAX = SHADER_STAGE_VERTEX_BIT | SHADER_STAGE_TESSELLATION_CONTROL_BIT | SHADER_STAGE_TESSELLATION_EVALUATION_BIT | SHADER_STAGE_GEOMETRY_BIT | SHADER_STAGE_FRAGMENT_BIT,
	SHADER_STAGE_TASK_MIN = SHADER_STAGE_TASK_BIT,
	SHADER_STAGE_TASK_MAX = SHADER_STAGE_TASK_BIT | SHADER_STAGE_MESH_BIT | SHADER_STAGE_FRAGMENT_BIT,
	SHADER_STAGE_RAY_TRACING_MIN = SHADER_STAGE_RAYGEN_BIT,
	SHADER_STAGE_RAY_TRACING_MAX = SHADER_STAGE_RAYGEN_BIT | SHADER_STAGE_ANY_HIT_BIT | SHADER_STAGE_CLOSEST_HIT_BIT | SHADER_STAGE_MISS_BIT | SHADER_STAGE_INTERSECTION_BIT | SHADER_STAGE_CALLABLE_BIT
};